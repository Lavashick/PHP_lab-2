        <h4>1.2 Язык программирования C++</h4>

        <p>C++ (произносится «си плюс плюс») -- компилируемый статически типизированный язык программирования общего назначения. Поддерживая разные парадигмы программирования, сочетает свойства как высокоуровневых, так и низкоуровневых языков.</p>

        <p>В сравнении с его предшественником -- языком C, -- наибольшее внимание уделено поддержке объектно-ориентированного и обобщённого программирования. Название «C++» происходит от языка C, в котором унарный оператор ++ обозначает инкремент переменной.</p>

        <p>Являясь одним из самых популярных языков программирования, C++ широко используется для разработки программного обеспечения. Область его применения включает создание операционных систем, разнообразных прикладных программ, драйверов устройств, приложений для встраиваемых систем, высокопроизводительных серверов, а также развлекательных приложений (например, видеоигры). Существует несколько реализаций языка C++ -- как бесплатных, так и коммерческих. Их производят Проект GNU, Microsoft, Intel и Embarcadero (Borland). C++ оказал огромное влияние на другие языки программирования, в первую очередь на Java и C#.</p>

        <p>При создании C++ Бьёрн Страуструп стремился сохранить совместимость с языком C. Множество программ, которые могут одинаково успешно транслироваться как компиляторами C, так и компиляторами C++, довольно велико -- отчасти благодаря тому, что синтаксис C++ был основан на синтаксисе C.</p>

        <h3>Глава 2. Описание программы</h3>

        <p>В процессе разработки проекта мною должна быть создана программа, имитирующая игру "Угадай число". Программа должна позволять играть игроку против компьютера. Компьютер загадывает четырехзначное число, все цифры которого различны (первая цифра числа отлична от нуля). Необходимо разгадать задуманное число. Игрок выигрывает когда отгадает задуманное число. Игрок вводит любое четырехзначное число и компьютер показывает количество угаданных цифр (цифра есть в записи задуманного числа, но не стоит в той же позиции, что и в задуманном числе) и количество стоящих на своем месте (цифра есть в записи задуманного числа и стоит в той же позиции, что и в задуманном числе).</p>

        <p>Например, если компьютер задумал число 8215 и введено число 1234, получаем в введенном числе одну угаданную цифру, и одну которая стоит на своем месте. Очевидно, что число отгадано в том случае, если угаданы все 4 числа.</p>

