        <h2 class="mt-3">Введение</h2>
        <p>Угадай число -- логическая игра для двоих игроков. Для игры достаточно иметь бумагу, ручку и уметь считать. Также игра может называться «цифры» или «цвета».</p>

        <h2>Правила игры</h2>

        <p>Играют двое. Каждый задумывает и записывает тайное 4-значное число с неповторяющимися цифрами. Игрок, который начинает игру по жребию, делает попытку отгадать число. Попытка -- это 4-значное число с неповторяющимися цифрами, сообщаемое противнику. Противник сообщает в ответ, сколько цифр угадано без совпадения с их позициями в тайном числе и сколько угадано вплоть до позиции в тайном числе. Например:</p>

        <p>Задумано тайное число «3219».</p>

        <p>Попытка номер: «2310».</p>

        <p>Результат: две «коровы» (две цифры: "2" и "3" -- угаданы на неверных позициях) и один «бык» (одна цифра "1" угадана вплоть до позиции).</p>

        <p>Игроки делают попытки угадать по очереди. Побеждает тот, кто угадает число первым.</p>

        <h2>Вариации игры</h2>

        <p>В игре «мастермайнд» (англ. Mastermind, возможный перевод: «гениальный отгадчик») загадывается последовательность из 4 цветных фишек, причём цвета могут повторяться.</p>

        <p>В усложнённом варианте может использоваться последовательность из 5, 6 или большего количества фишек.</p>

        <p>Существует вариант игры со словами. То есть игрок загадывает слово, обычно из 5 букв (в именительном падеже единственном числе по правилам игры «балда»), и задача противника -- угадать его, используя в качестве попыток такие же корректные слова из словаря русского языка.</p>

        <h2>Алгоритм</h2>

        <p>В общем случае количество вариантов для k-значного числа в N-ричной системе счисления без повторений, будет равно числу размещений</p>

        <p>Большинство известных алгоритмов суть вариации алгоритма полного перебора с определённой эвристикой. В связи с тем, что количество вариантов не столь велико и схема прямого перебора элементарно реализуется, компьютер играет в «быки и коровы» намного сильнее человека. Чем больше знаков в числе, тем больше разница в силе игры человека и компьютера.</p>

        <p>Как показал Дональд Кнут, для игры Mastermind (64 вариантов) при предложенной им стратегии нужно не более 5 попыток, чтобы отгадать любую комбинацию, и в среднем 4,34 попыток для отгадывания.</p>

        <p>В классическом случае игры с четырьмя не повторяющимися цифрами для отгадывания любого номера требуется не более семи ходов. Средняя минимальная длина игры составляет 26274/5040=5.2131 попытки.</p>

        <h2>Реализации</h2>

        <p>Существует множество вариантов компьютерной реализации игры, в том числе для мобильных телефонов и мобильных компьютеров.</p>

        <p>Настольные игры Mastermind популярны во всём мире. Наиболее распространены вариации:</p>
        <ul>
            <li>классическая, четыре не повторяющиеся цифры.</li>
            <li>обычная, 4 места для фишек 6 цветов с повторениями.</li>
            <li>продвинутая, 5 мест для фишек 8 цветов</li>
        </ul>

